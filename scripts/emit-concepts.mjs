/**
 * Deterministic emitter (kb→course engine): course-spec JSON → a SOUND concepts.ts.
 * The LLM keystone (synthesize-graph) proposes concepts + a justified prerequisite DAG;
 * this stage makes it pass the gates, mechanically and with a logged audit trail:
 *   - referential integrity (drop edges to non-concepts / self-loops)
 *   - stage monotonicity (a prereq must be same-or-earlier stage; drop violators)
 *   - ACYCLICITY (detect cycles, drop the closing back-edge, repeat — every drop logged)
 *   - primitive consistency (primitive ⇒ no prerequisites)
 *   - every surviving edge carries a why + a valid kind
 * Then it re-validates the emitted graph and reports. No human glue.
 *
 *   node scripts/emit-concepts.mjs <course-spec.json> [out.ts]   (default out: /tmp/gen-concepts.ts)
 */
import { readFileSync, writeFileSync } from "node:fs";

const KINDS = ["is-a", "part-of", "defined-via", "operates-on", "refines", "assumes"];
const specPath = process.argv[2];
const outPath = process.argv[3] || "/tmp/gen-concepts.ts";
if (!specPath) { console.error("usage: emit-concepts.mjs <spec.json> [out.ts]"); process.exit(2); }

const spec = JSON.parse(readFileSync(specPath, "utf8"));
const drops = [];
const log = (m) => drops.push(m);

// index concepts + stage→branch (the layer)
const byId = {};
for (const c of spec.concepts) byId[c.id] = c;
const stageBranch = {};
for (const s of spec.stages) stageBranch[s.id] = s.branch;
const stageIdx = (id) => byId[id]?.stageIdx ?? 999;
const primitives = new Set(spec.primitives || []);

// --- repair edges ---
for (const c of spec.concepts) {
  const kept = [];
  const seen = new Set();
  for (const e of c.prerequisites || []) {
    if (!byId[e.prereq]) { log(`${c.id}: drop edge → ${e.prereq} (not a concept)`); continue; }
    if (e.prereq === c.id) { log(`${c.id}: drop self-edge`); continue; }
    if (seen.has(e.prereq)) continue; seen.add(e.prereq);
    if (stageIdx(e.prereq) > c.stageIdx) { log(`${c.id}: drop edge → ${e.prereq} (prereq stage ${stageIdx(e.prereq)} > ${c.stageIdx})`); continue; }
    kept.push({ prereq: e.prereq, kind: KINDS.includes(e.kind) ? e.kind : "assumes", why: (e.why || "prerequisite").trim() });
  }
  c.prerequisites = kept;
}

// --- acyclicity: drop closing back-edges until acyclic ---
function findCycle() {
  const WHITE = 0, GREY = 1, BLACK = 2; const color = {};
  for (const c of spec.concepts) color[c.id] = WHITE;
  let cyc = null;
  const visit = (u, path) => {
    color[u] = GREY;
    for (const e of byId[u].prerequisites) {
      const v = e.prereq;
      if (color[v] === GREY) { cyc = [...path, u, v]; return; }
      if (color[v] === WHITE) { visit(v, [...path, u]); if (cyc) return; }
    }
    color[u] = BLACK;
  };
  for (const c of spec.concepts) { if (color[c.id] === WHITE) visit(c.id, []); if (cyc) break; }
  return cyc;
}
let guard = 0;
for (let cyc = findCycle(); cyc && guard < 10000; cyc = findCycle(), guard++) {
  const a = cyc[cyc.length - 2], b = cyc[cyc.length - 1]; // edge a→b closes the cycle
  byId[a].prerequisites = byId[a].prerequisites.filter((e) => e.prereq !== b);
  log(`acyclicity: drop edge ${a} → ${b} (closes cycle ${cyc.join("→")})`);
}

// --- primitive consistency ---
for (const c of spec.concepts) {
  c.primitive = primitives.has(c.id) && c.prerequisites.length === 0;
}

// --- build PREREQ maps ---
const prereqWhy = {}, prereqKind = {};
for (const c of spec.concepts) for (const e of c.prerequisites) {
  prereqWhy[`${c.id}>${e.prereq}`] = e.why;
  prereqKind[`${c.id}>${e.prereq}`] = e.kind;
}

// --- emit concepts.ts ---
const q = (s) => JSON.stringify(String(s ?? ""));
const arr = (a) => `[${a.map(q).join(", ")}]`;
const conceptLines = spec.concepts.map((c) => {
  const fields = [
    `id: ${q(c.id)}`, `term: ${q(c.term)}`, `layer: ${q(stageBranch[c.stage] || c.stage)}`,
    c.primitive ? "primitive: true" : null,
    `introducedIn: ${q(c.stage)}`,
    `short: ${q(c.short)}`,
    c.example ? `example: ${q(c.example)}` : null,
    `prerequisites: ${arr(c.prerequisites.map((e) => e.prereq))}`,
    `contrasts: []`,
  ].filter(Boolean);
  return `  { ${fields.join(", ")} },`;
}).join("\n");

const whyLines = Object.entries(prereqWhy).map(([k, v]) => `  ${q(k)}: ${q(v)},`).join("\n");
const kindLines = Object.entries(prereqKind).map(([k, v]) => `  ${q(k)}: ${q(v)},`).join("\n");

const ts = `/**
 * Concept dependency DAG — "${spec.meta?.domain || "course"}".
 * GENERATED by scripts/emit-concepts.mjs from a synthesize-graph course-spec. Source of
 * truth for the derived skill map, glossary, and debates. Acyclic; every edge justified.
 */
import type { Concept, ConceptGraph } from "../types";

const CONCEPTS: Concept[] = [
${conceptLines}
];

export const CONCEPT_GRAPH: ConceptGraph = { concepts: CONCEPTS };

export const PREREQ_WHY: Record<string, string> = {
${whyLines}
};

export function prereqWhy(concept: string, prereq: string): string | undefined {
  return PREREQ_WHY[\`\${concept}>\${prereq}\`];
}

export const PREREQ_KINDS = ${JSON.stringify(KINDS)} as const;
export type PrereqKind = (typeof PREREQ_KINDS)[number];

export const PREREQ_KIND: Record<string, PrereqKind> = {
${kindLines}
};

export function prereqKindOf(concept: string, prereq: string): PrereqKind | undefined {
  return PREREQ_KIND[\`\${concept}>\${prereq}\`];
}

export const CONCEPT_BY_ID: Record<string, Concept> = Object.fromEntries(
  CONCEPTS.map((c) => [c.id, c]),
);

export function conceptsForStage(lessonId: string): Concept[] {
  return CONCEPTS.filter((c) => c.introducedIn === lessonId);
}

export function conceptTopoOrder(): string[] {
  const visited = new Set<string>();
  const order: string[] = [];
  const visit = (id: string) => {
    if (visited.has(id)) return;
    visited.add(id);
    for (const p of CONCEPT_BY_ID[id]?.prerequisites ?? []) visit(p);
    order.push(id);
  };
  for (const c of CONCEPTS) visit(c.id);
  return order;
}

export function prerequisiteConceptsForStage(lessonId: string): Concept[] {
  const own = new Set(conceptsForStage(lessonId).map((c) => c.id));
  const prereqIds = new Set<string>();
  for (const c of conceptsForStage(lessonId))
    for (const p of c.prerequisites) if (!own.has(p)) prereqIds.add(p);
  return conceptTopoOrder().filter((id) => prereqIds.has(id)).map((id) => CONCEPT_BY_ID[id]).filter(Boolean);
}
`;
writeFileSync(outPath, ts);

// --- re-validate the emitted graph (the gate, standalone) ---
const errs = [];
const ids = new Set(spec.concepts.map((c) => c.id));
for (const c of spec.concepts) {
  for (const e of c.prerequisites) {
    if (!ids.has(e.prereq)) errs.push(`${c.id}: prereq ${e.prereq} not a concept`);
    if (!prereqWhy[`${c.id}>${e.prereq}`]) errs.push(`${c.id}>${e.prereq}: missing why`);
    if (!KINDS.includes(prereqKind[`${c.id}>${e.prereq}`])) errs.push(`${c.id}>${e.prereq}: bad kind`);
    if (stageIdx(e.prereq) > c.stageIdx) errs.push(`${c.id}: prereq ${e.prereq} later stage (coherence)`);
  }
  if (c.primitive && c.prerequisites.length) errs.push(`${c.id}: primitive with prerequisites`);
}
if (findCycle()) errs.push("graph still cyclic");

const nEdges = spec.concepts.reduce((n, c) => n + c.prerequisites.length, 0);
console.log(`\n--- emit-concepts ---`);
console.log(`concepts: ${spec.concepts.length} · stages: ${spec.stages.length} · edges: ${nEdges} · repairs: ${drops.length}`);
if (drops.length) { console.log("repairs (audit trail):"); for (const d of drops.slice(0, 40)) console.log("  - " + d); if (drops.length > 40) console.log(`  … +${drops.length - 40} more`); }
if (errs.length) { console.error(`\n✗ graph invalid: ${errs.length}`); for (const e of errs) console.error("  - " + e); process.exit(1); }
console.log(`\n✓ emitted ${outPath} — graph sound (acyclic, refs resolve, edges justified+kind, stage-monotone, primitives clean)`);
